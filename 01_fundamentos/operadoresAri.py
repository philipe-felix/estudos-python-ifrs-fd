# Operadores aritméticos com duas variáveis
pri_var = 5
seg_var = 3
resultado_soma = pri_var + seg_var
resultado_sub = pri_var - seg_var
resultado_mul = pri_var * seg_var
resultado_div_fra = pri_var / seg_var
resultado_div_int = pri_var // seg_var
resultado_div_resto = pri_var % seg_var
resultado_exp = pri_var ** seg_var
print("operações aritméticas realizadas com", pri_var, "e" ,seg_var)
print("soma:", resultado_soma)
print("subtração:", resultado_sub)
print("multiplicação:", resultado_mul)
print("divisão:", resultado_div_fra)
print("divisão inteira:", resultado_div_int)
print("resto da divisão:", resultado_div_resto)
print("exponenciação:", resultado_exp)